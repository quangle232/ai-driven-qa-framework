#!/usr/bin/env node
/**
 * create-gitlab-mr.js — open a GitLab merge request for a pushed branch
 * (qa-agent Phase 6.5: generated code reaches the team via branch + MR).
 *
 * Usage:
 *   node .claude/skills/qa-agent/scripts/create-gitlab-mr.js \
 *     --source test/EAST-123-login \
 *     --title  "EAST-123: Login — qa-agent generated tests" \
 *     [--target master] \
 *     [--description-file test-output/ai/mr-description.md]
 *
 * Branch naming (team rule): test/<STORY-KEY>-<feature-slug> for
 * story-driven runs; test/manual-<feature-slug>-<YYYYMMDD> for
 * gen-auto-test runs without a story.
 *
 * Config (same contract as trigger-gitlab.js): GITLAB_URL, GITLAB_TOKEN,
 * GITLAB_PROJECT_ID env vars, falling back to environments/.env.gitlab.
 *
 * Exit codes: 0 = MR created (URL printed), 1 = config missing,
 * 2 = usage error, 3 = GitLab API error.
 *
 * ESM module — package.json sets "type": "module". Node >= 18 (global fetch).
 */
import { existsSync, readFileSync } from 'fs';
import { dirname, join } from 'path';
import { fileURLToPath } from 'url';

const __dirname = dirname(fileURLToPath(import.meta.url));

function arg(name, def = undefined) {
    const i = process.argv.indexOf(`--${name}`);
    return i !== -1 && process.argv[i + 1] ? process.argv[i + 1] : def;
}

function findRepoRoot(start) {
    let dir = start;
    while (dir !== dirname(dir)) {
        if (existsSync(join(dir, 'package.json'))) return dir;
        dir = dirname(dir);
    }
    return process.cwd();
}

function loadConfig() {
    const fromFile = {};
    const file = join(findRepoRoot(__dirname), 'environments', '.env.gitlab');
    if (existsSync(file)) {
        for (const line of readFileSync(file, 'utf8').split('\n')) {
            const m = line.match(/^\s*([A-Z_]+)\s*=\s*(.*?)\s*$/);
            if (m) fromFile[m[1]] = m[2].replace(/^["']|["']$/g, '');
        }
    }
    return {
        url: (process.env.GITLAB_URL ?? fromFile.GITLAB_URL ?? '').replace(/\/+$/, ''),
        token: process.env.GITLAB_TOKEN ?? fromFile.GITLAB_TOKEN ?? '',
        projectId: process.env.GITLAB_PROJECT_ID ?? fromFile.GITLAB_PROJECT_ID ?? '',
    };
}

async function main() {
    const source = arg('source');
    const title = arg('title');
    const target = arg('target', 'master');
    const descriptionFile = arg('description-file');
    if (!source || !title) {
        console.error('usage: create-gitlab-mr.js --source <branch> --title <title> [--target master] [--description-file <md>]');
        process.exit(2);
    }

    const cfg = loadConfig();
    if (!cfg.url || !cfg.token || !cfg.projectId) {
        console.error(
            'GitLab config missing — set GITLAB_URL + GITLAB_TOKEN + GITLAB_PROJECT_ID env vars or fill environments/.env.gitlab. '
            + 'No MR was created.',
        );
        process.exit(1);
    }

    const description = descriptionFile && existsSync(descriptionFile)
        ? readFileSync(descriptionFile, 'utf8')
        : `Generated by the qa-agent skill. See docs/ai/memory.md for the run context.`;

    const response = await fetch(
        `${cfg.url}/api/v4/projects/${encodeURIComponent(cfg.projectId)}/merge_requests`,
        {
            method: 'POST',
            headers: { 'PRIVATE-TOKEN': cfg.token, 'Content-Type': 'application/json' },
            body: JSON.stringify({
                source_branch: source,
                target_branch: target,
                title,
                description,
                remove_source_branch: true,
            }),
        },
    );

    if (!response.ok) {
        const body = await response.text().catch(() => '<unreadable>');
        console.error(`GitLab MR creation failed: HTTP ${response.status} — ${body.slice(0, 500)}`);
        process.exit(3);
    }

    const mr = await response.json();
    console.log(`MR created: ${mr.web_url} (${source} -> ${target})`);
    process.exit(0);
}

main().catch((error) => {
    console.error('Unexpected error:', error);
    process.exit(3);
});
